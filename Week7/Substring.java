package Week7;

public class Substring {
    public static void main(String[] args) {
        String phrase = "Programming";
        System.out.println("Sub: " + phrase.substring(0,6));

        String word = "Computer";
        System.out.println("Sub: " + word.substring(0,3));

        String text = "Science";
        System.out.println("Sub: " + text.substring(3,7));
    }
    
}
